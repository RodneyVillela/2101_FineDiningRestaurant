    private void updateReceipt() {
    // Get the model from the cart table
    DefaultTableModel model = (DefaultTableModel) cartTable.getModel();

    // Clear the receipt table and populate it with the cart items
    DefaultTableModel receiptTableModel = (DefaultTableModel) jRecieptTable.getModel();
    receiptTableModel.setRowCount(0);  // Clear existing rows

    // Populate receipt table with cart items (Qty, Item, Price)
    for (int row = 0; row < model.getRowCount(); row++) {
        String item = model.getValueAt(row, 0).toString();
        double price = Double.parseDouble(model.getValueAt(row, 1).toString());
        int quantity = Integer.parseInt(model.getValueAt(row, 2).toString());
        double subtotal = price * quantity;
        
        // Add the item to the receipt table
        receiptTableModel.addRow(new Object[]{quantity, item, price, subtotal});
    }

    // Update the subtotal, tax, total, payment method, amount, and change in the receipt
    jRecieptSubTotal.setText(this.subTotalField.getText());
    jRecieptTax.setText(this.taxField.getText());
    jReceiptTotal.setText(this.totalField.getText());
    jRecieptAmountPayment.setText(this.paymentMethodField.getText());
    jRecieptAmountPayment1.setText(this.amountField.getText());
    jRecieptAmountPayment.setText(this.changeField.getText());
}
    


    
